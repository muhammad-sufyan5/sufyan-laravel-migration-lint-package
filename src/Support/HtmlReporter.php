<?php

namespace Sufyan\MigrationLinter\Support;

use Illuminate\Support\Str;

class HtmlReporter
{
    /**
     * Generate HTML report from issues.
     *
     * @param  Issue[]  $issues
     * @param  string|null  $outputPath
     * @return string
     */
    public function generate(array $issues, ?string $outputPath = null): string
    {
        $html = $this->buildHtmlDocument($issues);

        if ($outputPath) {
            $directory = dirname($outputPath);
            if (!file_exists($directory)) {
                mkdir($directory, 0755, true);
            }
            file_put_contents($outputPath, $html);
        }

        return $html;
    }

    /**
     * Build complete HTML document.
     */
    protected function buildHtmlDocument(array $issues): string
    {
        $stats = $this->calculateStats($issues);
        $groupedIssues = $this->groupIssuesByRule($issues);
        
        $timestamp = date('Y-m-d H:i:s');
        $projectName = basename(getcwd());

        return <<<HTML
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Laravel Migration Linter Report</title>
    <style>
        {$this->getStyles()}
    </style>
</head>
<body>
    <div class="container">
        <header class="header">
            <h1>üß© Laravel Migration Linter Report</h1>
            <div class="meta">
                <span class="project">Project: <strong>{$projectName}</strong></span>
                <span class="timestamp">Generated: {$timestamp}</span>
            </div>
        </header>

        {$this->renderSummary($stats)}
        {$this->renderChart($stats)}
        {$this->renderIssuesTable($issues)}
        {$this->renderGroupedSuggestions($groupedIssues)}
        {$this->renderRuleBreakdown($groupedIssues)}

        <footer class="footer">
            <p>Generated by <a href="https://github.com/muhammad-sufyan5/sufyan-laravel-migration-lint-package" target="_blank">Laravel Migration Linter</a></p>
        </footer>
    </div>

    <script>
        {$this->getJavaScript()}
    </script>
</body>
</html>
HTML;
    }

    /**
     * Calculate statistics from issues.
     */
    protected function calculateStats(array $issues): array
    {
        $files = array_unique(array_map(fn($i) => $i->file, $issues));
        
        return [
            'total_issues' => count($issues),
            'total_files' => count($files),
            'errors' => count(array_filter($issues, fn($i) => $i->severity === 'error')),
            'warnings' => count(array_filter($issues, fn($i) => $i->severity === 'warning')),
            'info' => count(array_filter($issues, fn($i) => $i->severity === 'info')),
            'files' => $files,
        ];
    }

    /**
     * Group issues by rule ID.
     */
    protected function groupIssuesByRule(array $issues): array
    {
        $grouped = [];
        foreach ($issues as $issue) {
            $grouped[$issue->ruleId][] = $issue;
        }
        return $grouped;
    }

    /**
     * Render summary section.
     */
    protected function renderSummary(array $stats): string
    {
        $statusClass = $stats['errors'] > 0 ? 'danger' : ($stats['warnings'] > 0 ? 'warning' : 'success');
        $statusIcon = $stats['errors'] > 0 ? '‚ùå' : ($stats['warnings'] > 0 ? '‚ö†Ô∏è' : '‚úÖ');
        $statusText = $stats['errors'] > 0 ? 'Action Required' : ($stats['warnings'] > 0 ? 'Review Recommended' : 'All Clear');

        return <<<HTML
        <div class="summary status-{$statusClass}">
            <div class="status-badge">
                <span class="status-icon">{$statusIcon}</span>
                <span class="status-text">{$statusText}</span>
            </div>
            <div class="stats-grid">
                <div class="stat-card">
                    <div class="stat-value">{$stats['total_issues']}</div>
                    <div class="stat-label">Total Issues</div>
                </div>
                <div class="stat-card">
                    <div class="stat-value">{$stats['total_files']}</div>
                    <div class="stat-label">Files Affected</div>
                </div>
                <div class="stat-card error">
                    <div class="stat-value">{$stats['errors']}</div>
                    <div class="stat-label">Errors</div>
                </div>
                <div class="stat-card warning">
                    <div class="stat-value">{$stats['warnings']}</div>
                    <div class="stat-label">Warnings</div>
                </div>
                <div class="stat-card info">
                    <div class="stat-value">{$stats['info']}</div>
                    <div class="stat-label">Info</div>
                </div>
            </div>
        </div>
HTML;
    }

    /**
     * Render chart section.
     */
    protected function renderChart(array $stats): string
    {
        $total = $stats['total_issues'];
        if ($total === 0) {
            return '';
        }

        $errorPercent = round(($stats['errors'] / $total) * 100);
        $warningPercent = round(($stats['warnings'] / $total) * 100);
        $infoPercent = round(($stats['info'] / $total) * 100);

        return <<<HTML
        <div class="chart-section">
            <h2>üìä Issue Distribution</h2>
            <div class="chart-container">
                <div class="bar-chart">
                    <div class="bar-item error" style="width: {$errorPercent}%">
                        <span class="bar-label">Errors: {$stats['errors']}</span>
                    </div>
                    <div class="bar-item warning" style="width: {$warningPercent}%">
                        <span class="bar-label">Warnings: {$stats['warnings']}</span>
                    </div>
                    <div class="bar-item info" style="width: {$infoPercent}%">
                        <span class="bar-label">Info: {$stats['info']}</span>
                    </div>
                </div>
            </div>
        </div>
HTML;
    }

    /**
     * Render issues table.
     */
    protected function renderIssuesTable(array $issues): string
    {
        if (empty($issues)) {
            return '<div class="no-issues">‚úÖ No issues found. Your migrations look safe!</div>';
        }

        $rows = '';
        foreach ($issues as $index => $issue) {
            $severityClass = $issue->severity;
            $severityBadge = $this->getSeverityBadge($issue->severity);
            $column = htmlspecialchars($issue->snippet ?? '-');
            $message = htmlspecialchars($issue->message);
            $file = htmlspecialchars(basename($issue->file));
            $ruleId = htmlspecialchars($issue->ruleId);
            $line = $issue->line ?? '-';

            $rows .= <<<HTML
                <tr class="severity-{$severityClass}" data-rule="{$ruleId}">
                    <td class="cell-severity">{$severityBadge}</td>
                    <td class="cell-file">{$file}</td>
                    <td class="cell-rule">{$ruleId}</td>
                    <td class="cell-column">{$column}</td>
                    <td class="cell-message">{$message}</td>
                    <td class="cell-line">{$line}</td>
                </tr>
HTML;
        }

        return <<<HTML
        <div class="issues-section">
            <h2>‚ö†Ô∏è Issues Found</h2>
            <div class="filters">
                <button class="filter-btn active" data-filter="all">All ({$this->calculateStats($issues)['total_issues']})</button>
                <button class="filter-btn" data-filter="error">Errors ({$this->calculateStats($issues)['errors']})</button>
                <button class="filter-btn" data-filter="warning">Warnings ({$this->calculateStats($issues)['warnings']})</button>
                <button class="filter-btn" data-filter="info">Info ({$this->calculateStats($issues)['info']})</button>
            </div>
            <div class="table-wrapper">
                <table class="issues-table">
                    <thead>
                        <tr>
                            <th>Severity</th>
                            <th>File</th>
                            <th>Rule</th>
                            <th>Column</th>
                            <th>Message</th>
                            <th>Line</th>
                        </tr>
                    </thead>
                    <tbody>
                        {$rows}
                    </tbody>
                </table>
            </div>
        </div>
HTML;
    }

    /**
     * Render grouped suggestions section.
     */
    protected function renderGroupedSuggestions(array $groupedIssues): string
    {
        $issuesWithSuggestions = array_filter($groupedIssues, function($issues) {
            return !empty($issues[0]->suggestion);
        });

        if (empty($issuesWithSuggestions)) {
            return '';
        }

        $suggestionsHtml = '';
        foreach ($issuesWithSuggestions as $ruleId => $issues) {
            $count = count($issues);
            $suggestion = nl2br(htmlspecialchars($issues[0]->suggestion));
            $docsUrl = $issues[0]->docsUrl ?? '';
            $docsLink = $docsUrl ? "<a href='{$docsUrl}' target='_blank' class='docs-link'>üìñ Documentation</a>" : '';

            $suggestionsHtml .= <<<HTML
            <div class="suggestion-card">
                <div class="suggestion-header">
                    <h3>üìã {$ruleId}</h3>
                    <span class="occurrence-badge">{$count} occurrence(s)</span>
                </div>
                <div class="suggestion-content">
                    <pre>{$suggestion}</pre>
                </div>
                {$docsLink}
            </div>
HTML;
        }

        return <<<HTML
        <div class="suggestions-section">
            <h2>üí° Suggestions & Recommendations</h2>
            {$suggestionsHtml}
        </div>
HTML;
    }

    /**
     * Render rule breakdown section.
     */
    protected function renderRuleBreakdown(array $groupedIssues): string
    {
        $rows = '';
        foreach ($groupedIssues as $ruleId => $issues) {
            $count = count($issues);
            $errors = count(array_filter($issues, fn($i) => $i->severity === 'error'));
            $warnings = count(array_filter($issues, fn($i) => $i->severity === 'warning'));
            $info = count(array_filter($issues, fn($i) => $i->severity === 'info'));

            $rows .= <<<HTML
            <tr>
                <td><strong>{$ruleId}</strong></td>
                <td class="text-center">{$count}</td>
                <td class="text-center severity-error">{$errors}</td>
                <td class="text-center severity-warning">{$warnings}</td>
                <td class="text-center severity-info">{$info}</td>
            </tr>
HTML;
        }

        return <<<HTML
        <div class="breakdown-section">
            <h2>üìà Rule Breakdown</h2>
            <table class="breakdown-table">
                <thead>
                    <tr>
                        <th>Rule</th>
                        <th>Total</th>
                        <th>Errors</th>
                        <th>Warnings</th>
                        <th>Info</th>
                    </tr>
                </thead>
                <tbody>
                    {$rows}
                </tbody>
            </table>
        </div>
HTML;
    }

    /**
     * Get severity badge HTML.
     */
    protected function getSeverityBadge(string $severity): string
    {
        $icons = [
            'error' => '‚ùå',
            'warning' => '‚ö†Ô∏è',
            'info' => '‚ÑπÔ∏è',
        ];

        $icon = $icons[$severity] ?? '‚Ä¢';
        return "<span class='severity-badge severity-{$severity}'>{$icon} " . ucfirst($severity) . "</span>";
    }

