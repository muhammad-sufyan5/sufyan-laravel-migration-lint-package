    protected function getJavaScript(): string
    {
        return <<<JS
const rows = Array.from(document.querySelectorAll('.issues-table tbody tr'));
const filterButtons = Array.from(document.querySelectorAll('.filter-btn'));
const searchInput = document.getElementById('searchInput');

function applyFilters() {
    if (!rows.length) {
        return;
    }

    const activeButton = document.querySelector('.filter-btn.active');
    const activeFilter = activeButton ? activeButton.dataset.filter : 'all';
    const query = searchInput ? searchInput.value.trim().toLowerCase() : '';

    rows.forEach((row) => {
        const rowSeverity = row.dataset.severity;
        const severityMatch = activeFilter === 'all' || rowSeverity === activeFilter;
        const textMatch = query === '' || row.textContent.toLowerCase().includes(query);
        row.classList.toggle('hidden', !(severityMatch && textMatch));
    });
}

filterButtons.forEach((button) => {
    button.addEventListener('click', () => {
        filterButtons.forEach((item) => item.classList.remove('active'));
        button.classList.add('active');
        applyFilters();
    });
});

if (searchInput) {
    searchInput.addEventListener('input', applyFilters);
}

applyFilters();
JS;
    }
