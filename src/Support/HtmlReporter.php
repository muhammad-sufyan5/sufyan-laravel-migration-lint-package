<?php

namespace Sufyan\MigrationLinter\Support;

class HtmlReporter
{
    /**
     * Generate HTML report from issues.
     *
     * @param  Issue[]  $issues
     * @param  string|null  $outputPath
     */
    public function generate(array $issues, ?string $outputPath = null): string
    {
        $html = $this->buildHtmlDocument($issues);

        if ($outputPath) {
            $directory = dirname($outputPath);
            if (! file_exists($directory)) {
                mkdir($directory, 0755, true);
            }

            file_put_contents($outputPath, $html);
        }

        return $html;
    }

    /**
     * Build complete HTML document.
     *
     * @param  Issue[]  $issues
     */
    protected function buildHtmlDocument(array $issues): string
    {
        $stats = $this->calculateStats($issues);
        $groupedIssues = $this->groupIssuesByRule($issues);
        $projectName = $this->escape(basename(getcwd()));
        $timestamp = date('Y-m-d H:i:s');

        return <<<HTML
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <title>Laravel Migration Linter Report</title>
    <style>
{$this->getStyles()}
    </style>
</head>
<body>
    <div class="page-shell">
        <main class="container">
            <header class="header">
                <div class="header-top">
                    <h1>Laravel Migration Linter Report</h1>
                    <span class="status-pill {$this->getOverallStatusClass($stats)}">{$this->getOverallStatusText($stats)}</span>
                </div>
                <div class="meta-grid">
                    <div class="meta-item">
                        <span class="meta-label">Project</span>
                        <strong class="meta-value">{$projectName}</strong>
                    </div>
                    <div class="meta-item">
                        <span class="meta-label">Generated</span>
                        <strong class="meta-value">{$timestamp}</strong>
                    </div>
                </div>
            </header>

            {$this->renderSummary($stats)}
            {$this->renderChart($stats)}
            {$this->renderIssuesTable($issues, $stats)}
            {$this->renderGroupedSuggestions($groupedIssues)}
            {$this->renderRuleBreakdown($groupedIssues)}

            <footer class="footer">
                <p>
                    Generated by
                    <a href="https://github.com/muhammad-sufyan5/sufyan-laravel-migration-lint-package" target="_blank" rel="noopener noreferrer">
                        Laravel Migration Linter
                    </a>
                </p>
            </footer>
        </main>
    </div>

    <script>
{$this->getJavaScript()}
    </script>
</body>
</html>
HTML;
    }

    /**
     * @param  Issue[]  $issues
     */
    protected function calculateStats(array $issues): array
    {
        $files = array_unique(array_map(fn ($issue) => $issue->file, $issues));

        return [
            'total_issues' => count($issues),
            'total_files' => count($files),
            'errors' => count(array_filter($issues, fn ($issue) => $issue->severity === 'error')),
            'warnings' => count(array_filter($issues, fn ($issue) => $issue->severity === 'warning')),
            'info' => count(array_filter($issues, fn ($issue) => $issue->severity === 'info')),
        ];
    }

    /**
     * @param  Issue[]  $issues
     */
    protected function groupIssuesByRule(array $issues): array
    {
        $grouped = [];

        foreach ($issues as $issue) {
            $grouped[$issue->ruleId][] = $issue;
        }

        return $grouped;
    }

    protected function renderSummary(array $stats): string
    {
        return <<<HTML
            <section class="card summary-card">
                <h2>Summary</h2>
                <div class="stats-grid">
                    <article class="stat-card">
                        <p class="stat-value">{$stats['total_issues']}</p>
                        <p class="stat-label">Total Issues</p>
                    </article>
                    <article class="stat-card">
                        <p class="stat-value">{$stats['total_files']}</p>
                        <p class="stat-label">Files Affected</p>
                    </article>
                    <article class="stat-card error">
                        <p class="stat-value">{$stats['errors']}</p>
                        <p class="stat-label">Errors</p>
                    </article>
                    <article class="stat-card warning">
                        <p class="stat-value">{$stats['warnings']}</p>
                        <p class="stat-label">Warnings</p>
                    </article>
                    <article class="stat-card info">
                        <p class="stat-value">{$stats['info']}</p>
                        <p class="stat-label">Info</p>
                    </article>
                </div>
            </section>
HTML;
    }

    protected function renderChart(array $stats): string
    {
        $total = $stats['total_issues'];

        if ($total === 0) {
            return '';
        }

        $errorPercent = max(1, (int) round(($stats['errors'] / $total) * 100));
        $warningPercent = max(1, (int) round(($stats['warnings'] / $total) * 100));
        $infoPercent = max(1, (int) round(($stats['info'] / $total) * 100));

        return <<<HTML
            <section class="card chart-section">
                <h2>Issue Distribution</h2>
                <div class="bar-chart">
                    <div class="bar-item error" style="width: {$errorPercent}%">
                        <span class="bar-label">Errors {$stats['errors']}</span>
                    </div>
                    <div class="bar-item warning" style="width: {$warningPercent}%">
                        <span class="bar-label">Warnings {$stats['warnings']}</span>
                    </div>
                    <div class="bar-item info" style="width: {$infoPercent}%">
                        <span class="bar-label">Info {$stats['info']}</span>
                    </div>
                </div>
            </section>
HTML;
    }

    /**
     * @param  Issue[]  $issues
     */
    protected function renderIssuesTable(array $issues, array $stats): string
    {
        if ($issues === []) {
            return '<section class="card"><div class="no-issues">No issues found. Your migrations look safe.</div></section>';
        }

        $rows = '';
        foreach ($issues as $issue) {
            $severity = $this->escape($issue->severity);
            $file = $this->escape(basename($issue->file));
            $ruleId = $this->escape($issue->ruleId);
            $column = $this->escape($issue->snippet ?? '-');
            $message = $this->escape($issue->message);
            $line = (int) ($issue->line ?? 0);
            $severityBadge = $this->getSeverityBadge($issue->severity);

            $rows .= <<<HTML
                <tr class="severity-{$severity}" data-severity="{$severity}" data-rule="{$ruleId}">
                    <td class="cell-severity">{$severityBadge}</td>
                    <td>{$file}</td>
                    <td><code>{$ruleId}</code></td>
                    <td><code>{$column}</code></td>
                    <td>{$message}</td>
                    <td>{$line}</td>
                </tr>
HTML;
        }

        return <<<HTML
            <section class="card issues-section">
                <div class="section-header">
                    <h2>Issues Found</h2>
                    <input type="text" id="searchInput" class="search-input" placeholder="Search by file, rule, message..." />
                </div>
                <div class="filters">
                    <button class="filter-btn active" data-filter="all">All ({$stats['total_issues']})</button>
                    <button class="filter-btn" data-filter="error">Errors ({$stats['errors']})</button>
                    <button class="filter-btn" data-filter="warning">Warnings ({$stats['warnings']})</button>
                    <button class="filter-btn" data-filter="info">Info ({$stats['info']})</button>
                </div>
                <div class="table-wrapper">
                    <table class="issues-table">
                        <thead>
                            <tr>
                                <th>Severity</th>
                                <th>File</th>
                                <th>Rule</th>
                                <th>Column</th>
                                <th>Message</th>
                                <th>Line</th>
                            </tr>
                        </thead>
                        <tbody>
{$rows}
                        </tbody>
                    </table>
                </div>
            </section>
HTML;
    }

    protected function renderGroupedSuggestions(array $groupedIssues): string
    {
        $issuesWithSuggestions = array_filter(
            $groupedIssues,
            fn (array $issues) => isset($issues[0]) && ! empty($issues[0]->suggestion)
        );

        if ($issuesWithSuggestions === []) {
            return '';
        }

        $cards = '';
        foreach ($issuesWithSuggestions as $ruleId => $issues) {
            $issueCount = count($issues);
            $safeRule = $this->escape((string) $ruleId);
            $safeSuggestion = nl2br($this->escape((string) $issues[0]->suggestion));
            $docsHtml = '';

            if (! empty($issues[0]->docsUrl)) {
                $safeUrl = $this->escape((string) $issues[0]->docsUrl);
                $docsHtml = "<a class=\"docs-link\" href=\"{$safeUrl}\" target=\"_blank\" rel=\"noopener noreferrer\">Open documentation</a>";
            }

            $cards .= <<<HTML
                <article class="suggestion-card">
                    <header class="suggestion-header">
                        <h3>{$safeRule}</h3>
                        <span class="occurrence-badge">{$issueCount} occurrence(s)</span>
                    </header>
                    <p class="suggestion-body">{$safeSuggestion}</p>
                    {$docsHtml}
                </article>
HTML;
        }

        return <<<HTML
            <section class="card suggestions-section">
                <h2>Suggestions & Recommendations</h2>
{$cards}
            </section>
HTML;
    }

    protected function renderRuleBreakdown(array $groupedIssues): string
    {
        if ($groupedIssues === []) {
            return '';
        }

        $rows = '';
        foreach ($groupedIssues as $ruleId => $issues) {
            $total = count($issues);
            $errors = count(array_filter($issues, fn ($issue) => $issue->severity === 'error'));
            $warnings = count(array_filter($issues, fn ($issue) => $issue->severity === 'warning'));
            $info = count(array_filter($issues, fn ($issue) => $issue->severity === 'info'));

            $safeRule = $this->escape((string) $ruleId);

            $rows .= <<<HTML
                <tr>
                    <td><code>{$safeRule}</code></td>
                    <td>{$total}</td>
                    <td class="severity-error">{$errors}</td>
                    <td class="severity-warning">{$warnings}</td>
                    <td class="severity-info">{$info}</td>
                </tr>
HTML;
        }

        return <<<HTML
            <section class="card breakdown-section">
                <h2>Rule Breakdown</h2>
                <div class="table-wrapper">
                    <table class="breakdown-table">
                        <thead>
                            <tr>
                                <th>Rule</th>
                                <th>Total</th>
                                <th>Errors</th>
                                <th>Warnings</th>
                                <th>Info</th>
                            </tr>
                        </thead>
                        <tbody>
{$rows}
                        </tbody>
                    </table>
                </div>
            </section>
HTML;
    }

    protected function getSeverityBadge(string $severity): string
    {
        $safeSeverity = $this->escape($severity);

        return "<span class=\"severity-badge severity-{$safeSeverity}\">".ucfirst($safeSeverity).'</span>';
    }

    protected function getOverallStatusClass(array $stats): string
    {
        if ($stats['errors'] > 0) {
            return 'is-danger';
        }

        if ($stats['warnings'] > 0) {
            return 'is-warning';
        }

        return 'is-success';
    }

    protected function getOverallStatusText(array $stats): string
    {
        if ($stats['errors'] > 0) {
            return 'Action required';
        }

        if ($stats['warnings'] > 0) {
            return 'Review recommended';
        }

        return 'All clear';
    }

    protected function escape(string $value): string
    {
        return htmlspecialchars($value, ENT_QUOTES, 'UTF-8');
    }

