    /**
     * Get JavaScript code.
     */
    protected function getJavaScript(): string
    {
        return <<<JS
        // Filter functionality
        document.querySelectorAll('.filter-btn').forEach(btn => {
            btn.addEventListener('click', function() {
                // Update active button
                document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
                this.classList.add('active');

                const filter = this.dataset.filter;
                const rows = document.querySelectorAll('.issues-table tbody tr');

                rows.forEach(row => {
                    if (filter === 'all') {
                        row.style.display = '';
                    } else {
                        row.style.display = row.classList.contains('severity-' + filter) ? '' : 'none';
                    }
                });
            });
        });

        // Add search functionality
        const searchHtml = '<div style="margin-bottom: 1rem;"><input type="text" id="searchInput" placeholder="Search issues..." style="width: 100%; padding: 0.75rem; border: 2px solid #e0e0e0; border-radius: 6px; font-size: 1rem;"></div>';
        document.querySelector('.filters').insertAdjacentHTML('afterend', searchHtml);

        document.getElementById('searchInput').addEventListener('input', function(e) {
            const searchTerm = e.target.value.toLowerCase();
            const rows = document.querySelectorAll('.issues-table tbody tr');

            rows.forEach(row => {
                const text = row.textContent.toLowerCase();
                row.style.display = text.includes(searchTerm) ? '' : 'none';
            });
        });
JS;
    }
